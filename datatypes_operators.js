// Exercise 1: Numbers

// Declaring a number variable and assigning an integer to it
const intVar = 5;

// Declaring  a variable and assigning it a floating number
const floatVar = 5.5;

// Perform arithmetic operations with a number variable and another number
console.log(intVar + floatVar);
console.log(intVar - floatVar);
console.log(intVar * floatVar);
console.log(intVar / floatVar);
console.log(intVar % 2); // Modulus
console.log(intVar ** 2); // Exponentiation

// Print all your variables
console.log("Integer: " + intVar);
console.log("Float: " + floatVar);


// Exercise 2: Boolean and Operators

// Use comparison operators to compare two numbers and store the results in boolean variables
const x = 8;
const y = 12;

console.log(x > y);  // false
console.log(x <= y); // true
console.log(x === y); // false
console.log(x !== y); // true

// Logical operations with booleans
const a = true;
const b = false;

console.log(a && b); // false
console.log(a || b); // true
console.log(!a); // false

// Using assignment operators
